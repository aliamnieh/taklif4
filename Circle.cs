using System;
using System.Collections.Generic;
using System.Text;

namespace mohit_masahat
{
    class Circle
    {
        private double radius;
        public double mohit;
        public double masahat;
        public Circle(double r)
        {
            radius = r;
            mohit = 2 * 3.14 * r;
            masahat = 3.14 * r * r;
        }
    }
}
